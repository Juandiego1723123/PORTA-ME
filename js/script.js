function applyLanguage(lang){
  document.documentElement.lang = lang;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const value = translations[lang][el.dataset.i18n];
    if (value !== undefined) el.textContent = value;
  });
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const value = translations[lang][el.dataset.i18nHtml];
    if (value !== undefined) el.innerHTML = value;
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const value = translations[lang][el.dataset.i18nPlaceholder];
    if (value !== undefined) el.placeholder = value;
  });
  const langToggle = document.getElementById('langToggle');
  if (langToggle) langToggle.textContent = lang === 'en' ? 'ES' : 'EN';
  const downloadCvBtn = document.getElementById('downloadCvBtn');
  if (downloadCvBtn) downloadCvBtn.href = lang === 'es'
    ? 'imagenes/CV-Juan-Diego-Edna-Smalbach-ES.pdf'
    : 'imagenes/CV-Juan-Diego-Edna-Smalbach-EN.pdf';
  localStorage.setItem('lang', lang);
}

document.addEventListener('DOMContentLoaded', () => {

  /* ===== Language gate ===== */
  const langGate = document.getElementById('langGate');
  const savedLang = localStorage.getItem('lang');
  if (savedLang){
    applyLanguage(savedLang);
    langGate.classList.add('hidden');
  }
  langGate.querySelectorAll('.lang-gate-btn').forEach(btn => {
    btn.addEventListener('click', () => {
      applyLanguage(btn.dataset.lang);
      langGate.classList.add('hidden');
    });
  });

  document.getElementById('langToggle').addEventListener('click', () => {
    const next = document.documentElement.lang === 'en' ? 'es' : 'en';
    applyLanguage(next);
  });

  /* ===== Preloader ===== */
  const preloader = document.getElementById('preloader');
  window.addEventListener('load', () => {
    setTimeout(() => preloader.classList.add('hidden'), 400);
  });
  // fallback in case 'load' already fired or is slow
  setTimeout(() => preloader.classList.add('hidden'), 1500);

  /* ===== Reduced motion preference ===== */
  const prefersReducedMotion = window.matchMedia('(prefers-reduced-motion: reduce)').matches;

  /* ===== Custom cursor ===== */
  const cursorDot = document.getElementById('cursorDot');
  const spotlight = document.getElementById('spotlight');
  let mouseX = 0, mouseY = 0, cursorTicking = false;

  window.addEventListener('mousemove', (e) => {
    mouseX = e.clientX; mouseY = e.clientY;
    if (cursorTicking) return;
    cursorTicking = true;
    requestAnimationFrame(() => {
      cursorDot.style.left = mouseX + 'px';
      cursorDot.style.top = mouseY + 'px';
      spotlight.style.setProperty('--x', mouseX + 'px');
      spotlight.style.setProperty('--y', mouseY + 'px');
      cursorTicking = false;
    });
  }, { passive: true });

  /* ===== Particles background ===== */
  const canvas = document.getElementById('particles');
  const ctx = canvas.getContext('2d');
  let particles = [];
  let width, height;

  function resizeCanvas(){
    width = canvas.width = window.innerWidth;
    height = canvas.height = window.innerHeight;
  }
  resizeCanvas();
  window.addEventListener('resize', resizeCanvas);

  function createParticles(){
    const count = Math.min(28, Math.floor((width * height) / 42000));
    const speed = prefersReducedMotion ? 0 : 0.3;
    particles = Array.from({ length: count }, () => ({
      x: Math.random() * width,
      y: Math.random() * height,
      vx: (Math.random() - 0.5) * speed,
      vy: (Math.random() - 0.5) * speed,
      r: Math.random() * 1.8 + 0.6
    }));
  }
  createParticles();
  window.addEventListener('resize', createParticles);

  const isLight = () => document.body.getAttribute('data-theme') === 'light';
  let particlesFrame, skipFrame = false;

  function drawParticles(){
    particlesFrame = requestAnimationFrame(drawParticles);
    // render at ~30fps instead of 60fps — these dots drift slowly, no visible difference
    skipFrame = !skipFrame;
    if (skipFrame) return;

    ctx.clearRect(0, 0, width, height);
    const dotColor = isLight() ? 'rgba(234,88,12,0.35)' : 'rgba(251,146,60,0.5)';

    for (const p of particles){
      p.x += p.vx; p.y += p.vy;
      if (p.x < 0 || p.x > width) p.vx *= -1;
      if (p.y < 0 || p.y > height) p.vy *= -1;
      ctx.beginPath();
      ctx.arc(p.x, p.y, p.r, 0, Math.PI * 2);
      ctx.fillStyle = dotColor;
      ctx.fill();
    }
  }
  particlesFrame = requestAnimationFrame(drawParticles);

  document.addEventListener('visibilitychange', () => {
    if (document.hidden){
      cancelAnimationFrame(particlesFrame);
    } else {
      particlesFrame = requestAnimationFrame(drawParticles);
    }
  });

  /* ===== Navbar scroll state + active link ===== */
  const navbar = document.getElementById('navbar');
  const sections = document.querySelectorAll('main section[id]');
  const navLinks = document.querySelectorAll('.nav-link');
  const backToTop = document.getElementById('backToTop');
  const scrollProgress = document.getElementById('scrollProgress');

  function onScroll(){
    navbar.classList.toggle('scrolled', window.scrollY > 40);

    let current = sections[0] ? sections[0].id : '';
    sections.forEach(sec => {
      if (window.scrollY >= sec.offsetTop - 140) current = sec.id;
    });
    navLinks.forEach(link => {
      link.classList.toggle('active', link.getAttribute('href') === '#' + current);
    });

    backToTop.classList.toggle('show', window.scrollY > 500);

    const docHeight = document.documentElement.scrollHeight - window.innerHeight;
    const progress = docHeight > 0 ? (window.scrollY / docHeight) * 100 : 0;
    scrollProgress.style.width = progress + '%';
  }
  let scrollScheduled = false;
  window.addEventListener('scroll', () => {
    if (scrollScheduled) return;
    scrollScheduled = true;
    requestAnimationFrame(() => { onScroll(); scrollScheduled = false; });
  }, { passive: true });
  onScroll();

  /* ===== Mobile menu ===== */
  const hamburger = document.getElementById('hamburger');
  const navLinksEl = document.getElementById('navLinks');
  hamburger.addEventListener('click', () => {
    hamburger.classList.toggle('active');
    navLinksEl.classList.toggle('active');
  });
  navLinksEl.querySelectorAll('a').forEach(a => a.addEventListener('click', () => {
    hamburger.classList.remove('active');
    navLinksEl.classList.remove('active');
  }));

  /* ===== Theme toggle ===== */
  const themeToggle = document.getElementById('themeToggle');
  const themeIcon = themeToggle.querySelector('i');
  const savedTheme = localStorage.getItem('theme') || 'dark';
  setTheme(savedTheme);

  themeToggle.addEventListener('click', (e) => {
    const next = document.body.getAttribute('data-theme') === 'dark' ? 'light' : 'dark';
    switchTheme(next, e.clientX, e.clientY);
    localStorage.setItem('theme', next);
  });

  function setTheme(theme){
    document.body.setAttribute('data-theme', theme);
    themeIcon.className = theme === 'dark' ? 'fa-solid fa-sun' : 'fa-solid fa-moon';
  }

  function switchTheme(theme, x, y){
    if (prefersReducedMotion || !document.startViewTransition){
      setTheme(theme);
      return;
    }
    const endRadius = Math.hypot(Math.max(x, window.innerWidth - x), Math.max(y, window.innerHeight - y));
    const transition = document.startViewTransition(() => setTheme(theme));
    transition.ready.then(() => {
      document.documentElement.animate(
        { clipPath: [`circle(0px at ${x}px ${y}px)`, `circle(${endRadius}px at ${x}px ${y}px)`] },
        { duration: 550, easing: 'ease-in-out', pseudoElement: '::view-transition-new(root)' }
      );
    });
  }

  /* ===== Scroll reveal ===== */
  const revealObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting){
        entry.target.classList.add('visible');
        revealObserver.unobserve(entry.target);
      }
    });
  }, { threshold: 0.15 });
  document.querySelectorAll('.reveal').forEach(el => revealObserver.observe(el));

  /* ===== Animated stat counters ===== */
  const statObserver = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting){
        animateCount(entry.target);
        statObserver.unobserve(entry.target);
      }
    });
  }, { threshold: 0.5 });
  document.querySelectorAll('.stat-number').forEach(el => statObserver.observe(el));

  function animateCount(el){
    const target = parseInt(el.dataset.count, 10);
    const suffix = el.dataset.suffix || '+';
    const suffixFinal = target === 100 ? '%' : '+';
    const duration = 1200;
    const start = performance.now();
    function tick(now){
      const progress = Math.min((now - start) / duration, 1);
      el.textContent = Math.floor(progress * target) + (progress >= 1 ? suffixFinal : '');
      if (progress < 1) requestAnimationFrame(tick);
    }
    requestAnimationFrame(tick);
  }

  /* ===== Back to top ===== */
  document.getElementById('backToTop').addEventListener('click', () => {
    window.scrollTo({ top: 0, behavior: 'smooth' });
  });

  /* ===== Project sliders ===== */
  document.querySelectorAll('.project-slider').forEach(slider => {
    const slides = Array.from(slider.querySelectorAll('.slide'));
    const dotsContainer = slider.querySelector('.slider-dots');
    let index = 0;
    let timer;

    slides.forEach((_, i) => {
      const dot = document.createElement('span');
      if (i === 0) dot.classList.add('active');
      dot.addEventListener('click', () => goTo(i));
      dotsContainer.appendChild(dot);
    });
    const dots = Array.from(dotsContainer.children);

    function goTo(i){
      slides[index].classList.remove('active');
      dots[index].classList.remove('active');
      index = (i + slides.length) % slides.length;
      slides[index].classList.add('active');
      dots[index].classList.add('active');
    }

    function next(){ goTo(index + 1); }
    function prev(){ goTo(index - 1); }

    function startAuto(){ if (!prefersReducedMotion) timer = setInterval(next, 3500); }
    function stopAuto(){ clearInterval(timer); }

    slider.querySelector('.next').addEventListener('click', () => { next(); stopAuto(); startAuto(); });
    slider.querySelector('.prev').addEventListener('click', () => { prev(); stopAuto(); startAuto(); });
    slider.addEventListener('mouseenter', stopAuto);
    slider.addEventListener('mouseleave', startAuto);

    startAuto();
  });

  /* ===== Contact form (front-end demo) ===== */
  const contactForm = document.getElementById('contactForm');
  const formStatus = document.getElementById('formStatus');
  contactForm.addEventListener('submit', async (e) => {
    e.preventDefault();
    const lang = document.documentElement.lang === 'es' ? 'es' : 'en';
    const submitBtn = contactForm.querySelector('button[type=submit]');
    const submitLabel = submitBtn.querySelector('[data-i18n="contact.send"]');
    const originalLabel = submitLabel.textContent;

    submitBtn.disabled = true;
    submitLabel.textContent = translations[lang]['contact.sending'];
    formStatus.textContent = '';

    const payload = {
      name: contactForm.name.value,
      email: contactForm.email.value,
      message: contactForm.message.value,
      company: contactForm.company.value,
    };

    try {
      const res = await fetch('/api/contact', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify(payload),
      });
      if (!res.ok) throw new Error('Request failed');
      formStatus.textContent = translations[lang]['contact.formSuccess'];
      contactForm.reset();
    } catch (err) {
      formStatus.textContent = translations[lang]['contact.formError'];
    } finally {
      submitBtn.disabled = false;
      submitLabel.textContent = originalLabel;
    }
  });

  /* ===== Footer year ===== */
  document.getElementById('year').textContent = new Date().getFullYear();

});
